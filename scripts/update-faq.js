// 直接更新数据库
const { PrismaClient } = require('@prisma/client')
const p = new PrismaClient()

async function update() {
  const existing = await p.systemConfig.findFirst()
  if (!existing) {
    await p.systemConfig.create({
      data: { key: 'site_settings', value: 'system', helpFaq: faqs }
    })
  } else {
    await p.systemConfig.update({
      where: { id: existing.id },
      data: { helpFaq: faqs }
    })
  }
  const updated = await p.systemConfig.findFirst({ select: { helpFaq: true } })
  console.log('FAQ 已更新，共', updated.helpFaq?.length || 0, '条')
  updated.helpFaq?.forEach((f, i) => console.log(`${i+1}. ${f.question.substring(0, 30)}...`))
}

update().catch(console.error).finally(() => p.$disconnect())
